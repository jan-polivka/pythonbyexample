# Define a function simply so
def hello_world():
    print("Hello World!")
    
# You can add parameters
def hello(name):
    print("Hello", name)
    
# You can return values
def add(a, b):
    return a + b

# To call the function, simply use its name and add round parenthesis,
# for example

hello_world()

